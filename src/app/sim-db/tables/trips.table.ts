import { Trip } from '@shared/models/logistics.models';

function parseCharge(raw: string | undefined): number {
  const t = String(raw ?? '').trim();
  if (!t) {
    return 0;
  }
  const n = Number(t.replace(/[^\d.-]/g, ''));
  return Number.isFinite(n) && n > 0 ? n : 0;
}

/** Estado inicial de cobro para demo (ingresos vs crédito en reportes). */
function enrichClientCollectionState(trips: Trip[]): Trip[] {
  return trips.map((t) => {
    if (t.hasClientBilling === false || parseCharge(t.clientCharge) <= 0) {
      return t;
    }
    if (t.clientCollectedAt !== undefined) {
      return t;
    }
    if (t.status === 'cancelled' && t.falseManeuver !== true) {
      return { ...t, clientCollectedAt: null };
    }
    if (t.status === 'completed') {
      const anchor = t.arrivedAt ?? t.returnAt ?? t.programmedAt;
      if (t.creditDays === 0) {
        return { ...t, clientCollectedAt: anchor };
      }
      const n = t.id.split('').reduce((acc, ch) => acc + ch.charCodeAt(0), 0);
      return { ...t, clientCollectedAt: n % 3 !== 0 ? anchor : null };
    }
    return { ...t, clientCollectedAt: null };
  });
}

